// Inicia 'i' em 100
let i = 100;
// Faz o seguinte bloco de código pelo menos uma vez
do {
    // Verifica se 'i' é múltiplo de 3
    if(i % 3 === 0) {
        // Imprime o número
        console.log(i);
    }
    // Diminui 'i' em 1 a cada iteração
    i--;
// Continua fazendo enquanto 'i' for maior ou igual a 0
} while(i >= 0);


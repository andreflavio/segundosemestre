// Inicia um loop que vai de 0 a 100
for(let i = 0; i <= 100; i++) {
    // Verifica se o número atual (i) é múltiplo de 3
    // O operador '%' retorna o resto da divisão de 'i' por 3
    // Se o resto é 0, então 'i' é múltiplo de 3
    if(i % 3 === 0) {
        // Imprime o número
        console.log(i);
    }
}
